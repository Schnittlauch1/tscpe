export const nop_instruction = [Operations.NOP, []];

function initializeOpcodeArray(arr) {
  for(let i=0; i < 0xFF; ++i) {
    arr[i] = nop_instruction;
  }
}

export const opcode = new Array(256);
initializeOpcodeArray(opcode);

